const calculator = {
    add: function(a, b){
        console.log(a + b);
    },

    minus: function(a, b){
        console.log(a - b);
    },

    mult: function(a, b){
        console.log(a * b);
    },

    div: function(a, b){
        console.log(a/b);
    },

    powerof: function(a, b){
        console.log(a**b);
    },
};

calculator.add(1,2);
calculator.minus(1,2);
calculator.mult(1,2);
calculator.div(1,2);
calculator.powerof(1,2);
